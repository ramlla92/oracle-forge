"""
schema_introspector.py
----------------------
Utility for introspecting database schemas across all four DAB database types
(PostgreSQL, MongoDB, SQLite, DuckDB) and returning a unified schema description
the agent can inject into its context window.

DAB Failure Categories addressed:
  - Ill-formatted join key mismatch (surfaces format differences between DBs)
  - Multi-database routing failure (identifies which tables/collections exist where)

Usage:
    from utils.schema_introspector import introspect_schema, introspect_all

    # Introspect a single database
    schema = introspect_schema("postgresql", connection_params)

    # Introspect all connected databases and return unified description
    full_schema = introspect_all(connections)

After running, paste the output into kb/domain/yelp_schema.md to fill in
the [ACTION REQUIRED] sections with confirmed column names and key formats.
"""

import json
import sqlite3
from typing import Any

import duckdb
import psycopg2
from pymongo import MongoClient


# ---------------------------------------------------------------------------
# Type alias for connection parameters
# ---------------------------------------------------------------------------
ConnectionParams = dict[str, Any]


def introspect_schema(db_type: str, params: ConnectionParams) -> dict:
    """
    Introspect the schema of a single database and return a structured description.

    Args:
        db_type:  One of "postgresql", "mongodb", "sqlite", "duckdb".
        params:   Connection parameters dict. Keys depend on db_type:
                  postgresql/sqlite/duckdb: {"host", "port", "database", "user", "password"}
                    or {"db_path"} for file-based databases.
                  mongodb: {"uri", "database"}

    Returns:
        dict with keys:
          "db_type"   — the database type string
          "tables"    — list of table/collection descriptions, each containing:
                        {"name", "columns": [{"name", "type", "sample_values"}]}
          "row_counts" — dict of table_name → approximate row count
          "errors"    — list of any errors encountered during introspection
    """
    introspectors = {
        "postgresql": _introspect_postgres,
        "mongodb":    _introspect_mongo,
        "sqlite":     _introspect_sqlite,
        "duckdb":     _introspect_duckdb,
    }

    fn = introspectors.get(db_type.lower())
    if fn is None:
        return {
            "db_type": db_type,
            "tables": [],
            "row_counts": {},
            "errors": [f"Unknown db_type '{db_type}'. Must be one of: {list(introspectors)}"],
        }

    return fn(params)


def introspect_all(connections: list[dict]) -> dict:
    """
    Introspect all connected databases and return a unified schema description
    suitable for injection into the agent's context window.

    Args:
        connections: List of dicts, each with keys:
                     {"db_type": str, "name": str, "params": ConnectionParams}
                     "name" is a human-readable label (e.g. "yelp_postgres").

    Returns:
        dict with keys:
          "databases"      — list of per-database schema dicts (from introspect_schema)
          "join_key_hints" — list of detected potential join key mismatches across DBs
          "summary"        — human-readable string suitable for KB injection
    """
    results = []
    for conn in connections:
        schema = introspect_schema(conn["db_type"], conn["params"])
        schema["name"] = conn.get("name", conn["db_type"])
        results.append(schema)

    hints = _detect_join_key_mismatches(results)
    summary = _build_summary(results, hints)

    return {
        "databases": results,
        "join_key_hints": hints,
        "summary": summary,
    }


def format_for_kb(introspection_result: dict) -> str:
    """
    Format the output of introspect_all() as a markdown string ready to paste
    into kb/domain/yelp_schema.md or equivalent domain KB document.

    Args:
        introspection_result: Output of introspect_all().

    Returns:
        Markdown-formatted string with confirmed schema details.
    """
    lines = ["# Confirmed Schema (generated by schema_introspector)\n"]
    for db in introspection_result["databases"]:
        lines.append(f"## {db.get('name', db['db_type'])} ({db['db_type']})\n")
        for table in db.get("tables", []):
            lines.append(f"### {table['name']}")
            lines.append(f"Row count: ~{db['row_counts'].get(table['name'], 'unknown')}\n")
            lines.append("| Column | Type | Sample Values |")
            lines.append("|--------|------|---------------|")
            for col in table.get("columns", []):
                raw_samples = ", ".join(str(v) for v in col.get("sample_values", []))
                samples = raw_samples[:120] + "..." if len(raw_samples) > 120 else raw_samples
                lines.append(f"| {col['name']} | {col['type']} | {samples} |")
            lines.append("")

    if introspection_result.get("join_key_hints"):
        lines.append("## Detected Join Key Mismatch Hints\n")
        for hint in introspection_result["join_key_hints"]:
            lines.append(f"- {hint}")
        lines.append("")

    return "\n".join(lines)


